import numpy as np

print("Arreglo unidimensional de 0")
print(np.zeros(shape=2,dtype=np.float32))

print("Arreglo bidimensionla de 1")
print(np.ones(shape=(2,5),dtype=np.float32))

print("Arreglo bidimensal vacio")
print(np.empty(shape=(3,3),dtype=np.int32))